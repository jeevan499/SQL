/* A Common Table Expression (CTE) is a temporary named result 
set that you can reference within a SELECT, INSERT, UPDATE, or DELETE 
statement in SQL. It's defined within the execution scope of a 
single SQL statement, meaning it's available only for the duration of 
that statement.

Here are some key points about CTEs:

1. Readability and Maintainability: CTEs can make complex SQL 
queries easier to read and maintain by breaking them down into smaller,
logical units. Instead of writing a single large and complicated SQL 
query, you can split it into multiple CTEs, each focusing on a 
specific part of the problem.





